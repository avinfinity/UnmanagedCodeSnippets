		void OverlappedVoxelsMarchingCubes::interpolateEdge3( float* p1, float* p2, float val_p1, float val_p2  , int levelP1 , int levelP2 , float* output )
		{
			
			float iso1 = IsoThresholds_[levelP1];
			float iso2 = IsoThresholds_[levelP2];

			float iso = ( iso1 + iso2 ) * 0.5;

			float w1 = val_p1 - iso;
			float w2 = iso - val_p2;

			//float mu =  //(_IsoLevel - val_p1) / (val_p2 - val_p1);

			//output[0] = p1[0] + mu * (p2[0] - p1[0]);
			//output[1] = p1[1] + mu * (p2[1] - p1[1]);
			//output[2] = p1[2] + mu * (p2[2] - p1[2]);

			if (w1 * w2 > 0)
			{
				output[0] = (p1[0] * w2 + p2[0] * w1) / (w1 + w2);
				output[1] = (p1[1] * w2 + p2[1] * w1) / (w1 + w2);
				output[2] = (p1[2] * w2 + p2[2] * w1) / (w1 + w2);
			}
			else
			{
				output[0] = (p1[0] + p2[0] ) * 0.5;
				output[1] = (p1[1] + p2[1]) * 0.5;
				output[2] = (p1[2] + p2[2]) * 0.5;
			}

		}



